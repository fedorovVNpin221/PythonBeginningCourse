# Задание №1
# Разработать программу, которая принимает на вход два числа с клавиатуры 
# и определяет наименьшее из них
def smallerNumber():
    num1 = float(input("Введите первое число: "))
    num2 = float(input("Введите второе число: "))

    if num1 < num2:
        print(f"Число {num1} меньше, чем {num2}!")
    elif num1 > num2:
        print(f"Число {num2} меньше, чем {num1}!")
    else:
        print("Числа равны!")

# Задание №2
# Разработать программу, которая определяет, есть ли пользователю 18 лет,
# то есть просит пользователя ввести возраст, и, если введенное число 18 и больше, 
# выводит сообщение о "Доступ разрешен!", если меньше 18 - "Доступ запрещен!"
def faceControl():
    age = int(input("Введите ваш возраст -> "))

    if (age < 18):
        print("Доступ запрещен!")
    else:
        print("Доступ разрешен!")

# Задание №3
# Разработать программу, которая выводит на экран все ЧЁТНЫЕ числа 
# от 1 до 100 и выводит результат в консоль
def evenNumber():
    num = 1
    while num != 100:
        if (num % 2 == 0):
            print(num)
        num += 1

# Задание №4
# Разработать программу, которая выводит на экран все НЕЧЁТНЫЕ числа 
# от 1 до 300 и выводит результат в консоль
def oddNumber():
    num = 1
    while num != 300:
        if (num % 2 != 0):
            print(num)
        num += 1

# Задача #5 
# Разработать программу "Калькулятор повторного использования" или "Циклический калькулятор".
# Программа должна поддерживать операции "сложение", "умножение", "деление", "вычитание" 
# и работать с двумя числами, которые пользователь вводит с клавиатуры.
# Сценарий использования: 
# 1. Ввод первого числа
# 2. Ввод второго числа
# 3. Ввод операции (+, -, *, /)
# 4. Вывод результата
# 5. Повторение всех операций, пока пользователь не введёт команду выхода из цикла
def calc():
    while True:
        num1 = int(input('1 число -> '))
        num2 = int(input('2 число -> '))

        operation = input('Введите операцию -> ')
        choice = input('Выполнить операцию? [y/n] -> ')

        if choice == 'y':
            if operation == '+':
                print(num1 + num2)
            elif operation == '-':
                print(num1 - num2)
            elif operation == '/':
                print(num1 / num2)
            elif operation == '*':
                print(num1 * num2)
            else:
                print('Неверная операция')
        else:
            print('Выход из программы')
            break

def main():
    while True:
        try:
            choice = int(input("Выберите программу (1-5): "))
            
            if choice == 1:
                print("Программа 'Наименьшее число'")
                smallerNumber()
            elif choice == 2:
                print("Программа 'Фэйс-контроль'")
                faceControl()
            elif choice == 3:
                print("Программа 'Чётные числа'")
                evenNumber()
            elif choice == 4:
                print("Программа 'Нечётные числа'")
                oddNumber()
            elif choice == 5:
                print("Программа 'Циклический калькулятор'")
                calc()
            else:
                print("Пожалуйста, введите число от 1 до 5")
                
        except ValueError:
            print("Ошибка: пожалуйста, введите целое число от 1 до 5")

if __name__ == "__main__":
    main()