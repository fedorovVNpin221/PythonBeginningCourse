import random # импортируем библиотеку рандом

# Задача №1
# Через цикл for посчитать сумму всех чисел в определенном диапазоне,
# диапазон вводится с клаиатуры.
# функция принимает на вход аргументы start и end
def summator(start, end):

    # если начало диапазона больше конца диапазона, 
    # то будем идти по диапазону в обратном порядке
    if start >= end:
        step = -1
    else:
        step = 1 

    sum = 0

    # range() принимает аргументы
    # start, stop, end в таком порядке:
    # range(start, stop, end)

    # цикл for, который проходится итерациями по всем значениям из диапазона,
    # то есть переменная item примет все значения от начала до конца диапазона
    for item in range(start, end, step):
        sum += item # прибавляем к уже имеющемуся значению в sum значение item с каждой итерацией

    print(sum)

