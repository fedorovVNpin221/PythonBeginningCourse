# Вычислить среднее арифметическое всех чисел в списке
def mediana():
    list = [1, 2, 78, 456, 90, 123]

    # проходимся по диапазону от 1 до 10(11 не входит в диапазон, это ограничитель)
    for item in range(1, 11):
        list.append(random.randint(1, 1000)) # добавляем в список случайное число от 1 до 1000
        # через точку используем функцию, которая характерна для определенного обьекта, например, 
        # списка list И библиотеки random

    count = 0
    sum = 0
    mediana = 0

    # проходимся по всем элементам списка
    # переменная item будет равна значению каждого элемента списка, 
    # пока не пройдемся по всему списку до конца
    for item in list:
        count += 1 # считаем количество элементов списка
        sum += item # складываем все элементы списка

    mediana = sum / count # находим среднее арифметическое
    print(mediana)

